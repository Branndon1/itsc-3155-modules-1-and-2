grade = int(input(" Enter a grade 0 - 100"))
if grade >= 90: print("Your grade is A")
elif grade >= 80: print("Your grade is B")
elif grade >= 70: print("Your grade is C")
elif grade >= 60: print("Your grade is D")
elif grade < 0: print("Your grade can't be negative!")
else: print ("Your grade is F")
 